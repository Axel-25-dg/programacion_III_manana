console.log("Operaciones RRHH: cálculos y comparaciones para métricas")

numero1 = 10
numero2 = 5
console.log("Suma - total ejemplo:")
suma = numero1 + numero2
console.log("Resultado de la suma:", suma)
console.log("Resta - diferencia:")
resta = numero1 - numero2
console.log("Resultado de la resta:", resta)
console.log("Multiplicación - producto:")
multiplicacion = numero1 * numero2
console.log("Resultado de la multiplicación:", multiplicacion)
console.log("División - promedio:")
division = numero1 / numero2
console.log("Resultado de la división:", division)

console.log("Módulo - residuo:")
modulo = numero1 % numero2
console.log("Resultado del módulo:", modulo)

console.log("Potencia - crecimiento estimado:")
potencia = numero1 ** numero2
console.log("Resultado de la potencia:", potencia)

console.log("Comparaciones de ejemplo (IDs, edades, etc.)")
console.log("igualdad débil ==", "5" == 5)
console.log("igualdad estricta ===", "5" === 5)
console.log("mayor que >", 5 > 3)
console.log("menor que <", 5 < 3)
console.log("mayor o igual que >=", 5 >= 3)
console.log("menor o igual que <=", 5 <= 3)

console.log("Operadores lógicos (ejemplos de condiciones)")
console.log("y AND &&", "5" <= 5 && "5" >= 5)
console.log("o OR ||", "6" === 6 || "6" < 6)
console.log("negación NOT !", !("6" === 6))


console.log("Incremento (ejemplo de ajuste)")
numero1++
console.log("Resultado del incremento:", numero1)